#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	int		i2;

	i = 0;
	i2 = 0;
	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s);
		while (i2 < i)
		{
			(*f)(s);
			s++;
			i2++;
		}
	}
}
