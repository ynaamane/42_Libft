#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		i;
	int		i2;

	if (s != NULL && f != NULL)
	{
		i2 = 0;
		i = ft_strlen(s);
		while (i2 < i)
		{
			(*f)(s);
			s++;
			i2++;
		}
	}
}
